class MagicSpot(Spot):
    def __init__(self):
        super().__init__(False, True, True)
        self.setRegion(1, 0, 0, 54, 0, 55)

    def setBlock(self, x, y, z, blockId, face):
        if y != 0 or (blockId != 66 and blockId != 0): #66: Rail
            return

        block1 = super().getBlock(x+1, y, z)
        block2 = super().getBlock(x-1, y, z)
        block3 = super().getBlock(x, y, z+1)
        block4 = super().getBlock(x, y, z-1)
        block = super().getBlock(x, y, z)

        if block1 != 66 and block2 != 66 and block3 != 66 and block4 != 66:
            if x != 1 or z != 0:
                return
        
        if  block == 0 or block == 57: #57: Diamond
            super().setBlock(x, y, z, blockId, face)

        if block == 66 and blockId == 0: #66: Rail
            super().setBlock(x, y, z, blockId, face)

    def resetMaze(self, mc, x0, z0, x1, z1, y0, height):
        if x0 < x1:
            stepx = 1
        else:
            stepx = -1

        if z0 < z1:
            stepz = 1
        else:
            stepz = -1

        dx = 0
        dz = 0
        for x in range(x0, x1+stepx, stepx):
            for z in range(z0, z1+stepz, stepz):
                
                for y in range (y0, y0+height):
                    if maze[dx][dz] == 0:
                        mc.setBlock(x, y, z, 24)
                        
                    elif dx != 0:
                        if y == y0:
                            mc.setBlock(x, y, z, 24)
                        else:
                            mc.setBlock(x, y, z, 0)
                
                dz += 1
            dz = 0
            dx += 1

    def reset(self, blockId = 24):
        self.resetMaze(World.pcMinecraft, 2848-54, -57, 2848, 0, 62, 2)
        World.pcMinecraft.setBlock(2802,63,-1, 57) #57: diamond